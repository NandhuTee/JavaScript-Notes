//Implicit Coercion 
console.log('5' + 2); // Outputs '52' (number 2 is coerced to a string)
console.log('5' - 2); // Outputs 3 (string '5' is coerced to a number)

// Explicit Coercion 

let str = "123";
let num = Number(str); // Explicit conversion from string to number
console.log(num); // Outputs 123

